package viikko2.toistorakenteet;

public class ForToistokaskyLaskevillaNumeroilla {

    public static void main(String[] args) {
        // lasketaan luvut alaspäin kymmenestä yhteen!

        // "kasvatus" voi yhtä hyvin tehdä myös vähennys- tai muita operaatioita
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
